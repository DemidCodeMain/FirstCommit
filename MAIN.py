a = int(input("Введите , пожалуйста , число:"))
b = input("Введите операцию:")
c = int(input("Введите , пожалуйста , второе число:"))
if b == '+':
    print(a+c)
if b == '-':
    print(a-c)
if b == '*':
    print(a*c)
if b == '/':
    print(a/c)
if b == '%':
    print(a%c)
if b == '**':
    print(a**c)